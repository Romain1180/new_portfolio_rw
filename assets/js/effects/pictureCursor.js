let basicCursor = document.querySelector('.custom-cursor');

let lineProcess1 = document.querySelector('.line-1');

lineProcess1.addEventListener('mouseover', function(){
  basicCursor.style.backgroundImage = 'url(./assets/img/conception.svg)';
  basicCursor.style.backgroundRepeat = 'no-repeat';
  basicCursor.style.backgroundSize = 'cover';
  basicCursor.style.borderRadius = '0%';
  basicCursor.style.width = '173px';
  basicCursor.style.height = '245px';  
})
lineProcess1.addEventListener('mouseout', function(){
  basicCursor.style.backgroundImage = 'none';
  basicCursor.style.borderRadius = '50%';
  basicCursor.style.width = '25px';
  basicCursor.style.height = '25px';
})


